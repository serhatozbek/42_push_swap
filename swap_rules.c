void	sa(t_stack *stack)
{
	int	tmp;

	if (stack->a_size > 1)
	{
		tmp = stack->a[0];
		stack->a[0] = stack->a[1];
		stack->a[1] = tmp;
		write(1, "sa\n", 3);
	}
}
